#include <stdio.h>
/**
 * main - prints sizes of computers
 * Return: always 0 (success)
 *
*/
int main(void)
{
	printf("size of char: %lu byte(a)\n", sizeof(a));
	printf("size of an int: %lu byte(b)\n", sizeof(b));
	printf("size of long int: %lu byte(c)\n", sizeof(c));
	printf("size of long long: %lu byte(d)\n", sizeof(d));
	printf("size of float: %lu byte(f)\n", sizeof(f));
	return (0);
}
